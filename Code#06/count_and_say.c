#include <stdio.h>
#include <unistd.h>

int main (int ac, char **av)
{
    if (ac == 2)
    {
        int i = 0;
        while (av[1][i] != '\0')
        {
            int count = 1;
            while (av[1][i] == av[1][i + 1])
            {
                count++;
                i++;
            }
            printf("%d%c ", count, av[1][i]);
            i++;
        }
    }
    return (0);
}